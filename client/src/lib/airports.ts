export const airports: Airport[] = data.map(([code, city, name]) => ({ code, city, name }));

export function searchAirports(query: string, limit = 5): Airport[] {
	const q = query.trim().toUpperCase();
	const ql = q.toLowerCase();
	if (!q) return [];

	const exact: Airport[] = [];
	const starts: Airport[] = [];
	const contains: Airport[] = [];

	const seen = new Set<string>();

	for (const a of airports) {
		const key = a.code + a.city;
		if (seen.has(key)) continue;

		if (a.code === q) {
			exact.push(a);
			seen.add(key);
		} else if (a.code.startsWith(q)) {
			starts.push(a);
			seen.add(key);
		} else if (a.city.toLowerCase().includes(ql) || a.name.toLowerCase().includes(ql)) {
			contains.push(a);
			seen.add(key);
		}
	}

	return [...exact, ...starts, ...contains].slice(0, limit);
}
